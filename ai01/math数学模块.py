import math

print(math.e) # 自然底数
print(math.pi) # 圆周率
print(math.acos(0.1)) # 反余弦
print(math.cos(90)) # 余弦
print(math.degrees(50)) # 角度值
print(math.factorial(5)) # 阶乘
print(math.pow(5, 2)) # 幂函数
print(math.sqrt(9)) # 开方
print(math.hypot(5, 6)) # 欧氏距离(两点之间最短的距离)
print(math.fabs(-5)) # 绝对值
print(math.floor(3.14)) # 向下取整
print(math.ceil(3.14)) # 向上取整